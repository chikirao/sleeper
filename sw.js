const CACHE_NAME = "sleeper-v2";
const ASSET_PATHS = [
  "/sleeper/",
  "/sleeper/assets/index.css",
  "/sleeper/assets/index.js",
  "/sleeper/manifest.webmanifest",
  "/sleeper/logo.png",
  "/sleeper/icon-512.png",
  "/sleeper/apple-touch-icon.png",
  "/sleeper/favicon.png"
];

self.addEventListener("install", (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(ASSET_PATHS)),
  );
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((keys) =>
        Promise.all(keys.filter((key) => key !== CACHE_NAME).map((key) => caches.delete(key))),
      ),
  );
  self.clients.claim();
});

self.addEventListener("fetch", (event) => {
  if (event.request.method !== "GET") {
    return;
  }

  event.respondWith(
    caches.match(event.request).then((cached) => {
      if (cached) {
        return cached;
      }

      return fetch(event.request)
        .then((response) => {
          const responseClone = response.clone();
          caches.open(CACHE_NAME).then((cache) => cache.put(event.request, responseClone));
          return response;
        })
        .catch(() => caches.match("/sleeper/"));
    }),
  );
});
